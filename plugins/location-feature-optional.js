const { withAndroidManifest } = require("expo/config-plugins");

/**
 * Config plugin that marks the location hardware features as OPTIONAL.
 *
 * We request ACCESS_FINE_LOCATION / ACCESS_COARSE_LOCATION (via expo-location)
 * only to read the current WiFi SSID for auto-offline. Android's implicit
 * <uses-feature> rules turn those permissions into *required* hardware features
 * unless we say otherwise:
 *   - ACCESS_FINE_LOCATION   -> android.hardware.location.gps      (required)
 *   - ACCESS_COARSE_LOCATION -> android.hardware.location.network  (required)
 *   - either                 -> android.hardware.location          (required)
 *
 * The Play Store filters the app off any device lacking a required feature, so
 * GPS-less Android media players (e.g. the cheap 5" Android-13 boxes some users
 * run — "this app is not available for your device") get excluded even though
 * the app never needs a GPS fix. Marking each location feature required="false"
 * keeps the permissions while telling Play the hardware is optional, restoring
 * compatibility with those devices. The app already degrades gracefully when
 * location/SSID is unavailable (auto-offline just can't key on the network).
 *
 * Must be a config plugin because android/ is regenerated by expo prebuild and
 * expo-build-properties can't add <uses-feature> entries.
 */
const OPTIONAL_FEATURES = [
  "android.hardware.location",
  "android.hardware.location.gps",
  "android.hardware.location.network",
];

function withLocationFeatureOptional(config) {
  return withAndroidManifest(config, (config) => {
    const manifest = config.modResults.manifest;
    const features = (manifest["uses-feature"] = manifest["uses-feature"] || []);

    for (const name of OPTIONAL_FEATURES) {
      const existing = features.find((f) => f.$ && f.$["android:name"] === name);
      if (existing) {
        existing.$["android:required"] = "false";
      } else {
        features.push({
          $: { "android:name": name, "android:required": "false" },
        });
      }
    }

    return config;
  });
}

module.exports = withLocationFeatureOptional;
